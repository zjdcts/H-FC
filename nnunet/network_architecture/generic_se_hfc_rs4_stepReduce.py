class Upsample(nn.Module):
    def __init__(self, size=None, scale_factor=None, mode='nearest', align_corners=False):
        super(Upsample, self).__init__()
        self.align_corners = align_corners
        self.mode = mode
        self.scale_factor = scale_factor
        self.size = size

    def forward(self, x):
        return nn.functional.interpolate(x, size=self.size, scale_factor=self.scale_factor, mode=self.mode,
                                         align_corners=self.align_corners)


class ChAtt(nn.Module):
    def __init__(self, input_channels, reduction=2):
        super(ChAtt, self).__init__()
        self.avg_pool = nn.AdaptiveAvgPool3d(1)
        # self.max_pool = nn.AdaptiveMaxPool3d(1)
        self.avg_fc1 = nn.Linear(input_channels, input_channels // reduction, bias=True)
        # self.max_fc1 = nn.Linear(input_channels, input_channels // reduction)
        self.avg_relu = nn.ReLU()
        # self.max_relu = nn.ReLU(inplace=True)
        self.avg_fc2 = nn.Linear(input_channels // reduction, input_channels, bias=True)
        # self.max_fc2 = nn.Linear(input_channels // reduction, input_channels)
        self.sigmoid = nn.Sigmoid()

    def forward(self, x):
        b, c, _, _, _ = x.size()
        avg_pool = self.avg_pool(x).view(b, c)
        # max_pool = self.max_pool(x).view(b, c)
        avg_pool = self.avg_fc2(self.avg_relu(self.avg_fc1(avg_pool)))
        # max_pool = self.max_fc2(self.max_relu(self.max_fc1(max_pool)))
        y = torch.mul(x, self.sigmoid(avg_pool).view(b, c, 1, 1, 1))
        return y


class FeedForward(nn.Module):
    def __init__(self, dim, hidden_dim, out_dim, drop=0.1):
        super().__init__()
        self.net = nn.Sequential(
            nn.Linear(dim, hidden_dim, bias=True),
            nn.ReLU(),
            nn.Linear(hidden_dim, out_dim, bias=True),
            nn.ReLU(),
            nn.Dropout(drop),
        )

    def forward(self, x):
        return self.net(x)


